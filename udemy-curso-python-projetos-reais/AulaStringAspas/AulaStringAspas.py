if __name__ == '__main__':
    print('Strings (texto) e aspas em Python')

    """
    str - string
    """

    print("Aspas duplas")
    print('Aspas simples')

    # Uso de aspas dentro da string
    print('Essa é uma "string" (str)')
    print("Essa é uma 'string' (str)")

    # Uso de caracteres de escape
    print('Esse é meu \'texto\' (str)')

    # Quebra de linha
    print('Esse é meu \n texto ')

    # Informa para o interpretador que o conteúdo da string não deve ser ignorado
    print(r'Esse é meu texto com \n"aspas')
